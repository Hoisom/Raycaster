def test_get_dt():
    with pytest.raises(TypeError):
        assert get_dt()
    with pytest.raises(TypeError):
        assert get_dt("cat")

def test_fix_colour():
    with pytest.raises(TypeError):
        assert fix_colour()
    assert fix_colour((255, 20, 0)) == (255, 20, 0)
    assert fix_colour((-255, 20, 0)) == (0, 20, 0)
    assert fix_colour((-55, -20,-70)) == (0, 0, 0)

